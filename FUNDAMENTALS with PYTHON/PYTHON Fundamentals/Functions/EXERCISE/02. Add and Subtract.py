def sum_numbers(first_num, second_num):
    return first_num + second_num
def subtract(sum, third_num):
    return sum - third_number

def add_and_substract(first, second, third):
    sum_of_first_and_second = sum_numbers(first, second)
    result = subtract(sum_of_first_and_second, third)
    return result
first_number = int(input())
second_number = int(input())
third_number = int(input())

print(add_and_substract(first_number, second_number, third_number))